package co.id.keriss.fastpay.beans.respone;

public class GantiPinRes {

	
	private String uid;
	private String pinlama;
	private String pinbaru;
	private String status;
	private String keterangan;
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getPinlama() {
		return pinlama;
	}
	public void setPinlama(String pinlama) {
		this.pinlama = pinlama;
	}
	public String getPinbaru() {
		return pinbaru;
	}
	public void setPinbaru(String pinbaru) {
		this.pinbaru = pinbaru;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getKeterangan() {
		return keterangan;
	}
	public void setKeterangan(String keterangan) {
		this.keterangan = keterangan;
	}
	
	
	
}
